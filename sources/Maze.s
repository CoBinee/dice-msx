    inc     hl
    ld      d, (hl)
    inc     hl
    ld      c, (hl)
    inc     hl
    ld      b, (hl)
    inc     hl
    ex      de, hl

    ; 壁の描画
10$:
    push    bc
    push    hl
11$:
    ld      a, (de)
    ld      (hl), a
    inc     de
    inc     hl
    dec     c
    jr      nz, 11$
    pop     hl
    ld      bc, #0x0020
    add     hl, bc
    pop     bc
    djnz    10$

    ; レジスタの復帰
    pop     de
    pop     bc
    pop     hl

    ; 終了
    ret

