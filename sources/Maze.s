; 迷路を初期化する
;
_MazeInitialize::
    
    ; レジスタの保存
    
    ; 迷路の初期化
    xor     a
    call    _MazeLoad

    ; レジスタの復帰
    
    ; 終了
    ret

; 迷路を読み込む
;
_MazeLoad:

    ; レジスタの保存

    ; a < 現在地のブロック

    ; 迷路の初期化
    ld      hl, #(_maze + 0x0000)
    ld      de, #(_maze + 0x0001)
    ld      bc, #(MAZE_SIZE_X * MAZE_SIZE_Y - 0x0001)
    ld      (hl), #MAZE_PATH
    ldir

    ; スタート地点の設定
    call    _MazeLoadHere

    ; レジスタの復帰

    ; 終了
    ret

_MazeLoadHere::

    ; レジスタの保存

    ; a < 現在地のブロック

    ; 迷路の設定
    add     a, a
    add     a, a
    add     a, a
    ld      e, a
    ld      d, #0x00
    ld      hl, #mazeBlockHere
    add     hl, de
    ld      de, #(_maze + MAZE_BLOCK_SIZE_Y * MAZE_SIZE_X)
    call    MazeSetBlock

    ; レジスタの復帰

    ; 終了
    ret

_MazeLoadNext::

    ; レジスタの保存

    ; a < 目的地のブロック

    ; 迷路の設定
    add     a, a
    add     a, a
    add     a, a
    ld      e, a
    ld      d, #0x00
    ld      hl, #mazeBlockNext
    add     hl, de
    ld      de, #(_maze + 0 * MAZE_SIZE_X)
    call    MazeSetBlock

    ; レジスタの復帰

    ; 終了
    ret

; 迷路にブロックを設定する
;
MazeSetBlock:

    ; レジスタの保存
    push    hl
    push    bc
    push    de

    ; hl < ブロック
    ; de < 迷路の位置

    ; 迷路の展開
    ex      de, hl
    ld      b, #MAZE_BLOCK_SIZE_Y
10$:
    push    bc
    ld      a, (de)
    ld      b, #MAZE_BLOCK_SIZE_X
11$:
    rla
    jr      nc, 12$
    ld      (hl), #MAZE_WALL
12$:
    inc     hl
    djnz    11$
    inc     de
    ld      bc, #(MAZE_SIZE_X - MAZE_BLOCK_SIZE_X)
    add     hl, bc
    pop     bc
    djnz    10$

    ; レジスタの復帰
    pop     de
    pop     bc
    pop     hl

    ; 終了
    ret

; 壁かどうかを判定する
;
_MazeIsWall::

    ; レジスタの保存
    push    hl
    push    de

    ; de < Y/X 位置
    ; cf > 1 = 壁

    ; 壁の判定
    ld      a, e
    cp      #MAZE_SIZE_X
    jr      nc, 18$
    ld      a, d
    cp      #MAZE_SIZE_Y
    jr      nc, 18$
    add     a, a
    add     a, a
    add     a, a
    add     a, a
    add     a, e
    ld      e, a
    ld      d, #0x00
    ld      hl, #_maze
    add     hl, de
    ld      a, (hl)
    or      a
    jr      z, 18$
    scf
    jr      19$
18$:
    or      a
;   jr      19$
19$:

    ; レジスタの復帰
    pop     de
    pop     hl

    ; 終了
    ret

; 現在地の位置を取得する
;
_MazeGetHerePosition::

    ; レジスタの保存

    ; a  < ブロック
    ; de > Y/X 位置

    ; 位置の取得
    ld      de, #(((MAZE_BLOCK_SIZE_Y + MAZE_BLOCK_O_Y) << 8) | MAZE_BLOCK_O_X)

    ; レジスタの復帰

    ; 終了
    ret

; 現在地の向きを取得する
;
_MazeGetHereDirection::

    ; レジスタの保存
    push    hl
    push    de

    ; a < ブロック
    ; a > 向き

    ; 向きの取得
    ld      e, a
    ld      d, #0x00
    ld      hl, #mazeHereDirection
    add     hl, de
    ld      a, (hl)

    ; レジスタの復帰
    pop     de
    pop     hl

    ; 終了
    ret

